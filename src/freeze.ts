// Окно «оседания результата» перед заморозкой. «Завершено» ≠ «окончательно»:
// у команд ~36ч на апелляцию (FIA) + у FIA ~36ч на вердикты по отложенным
// нарушениям, плюс возможное расследование. Поэтому после финиша сессии ещё
// какое-то время ТЯНЕМ результат каждый прогон (штраф/апелляция подхватятся,
// writeIfChanged закоммитит только при реальном изменении), и лишь по истечении
// окна — freeze (не рескрейпим). Одна тюнящаяся константа на все продьюсеры.

// 7 дней: покрывает апелляцию 36ч + вердикты 36ч (72ч) с запасом на расследование.
export const FREEZE_AFTER_MS = 7 * 24 * 3600 * 1000;

/// finishMs — момент финиша (ISO-инстант последней сессии/дня гонки), now — Date.now().
/// true → результат «оседал» дольше окна → можно морозить.
export function isFrozen(finishMs: number | null, now: number): boolean {
  return finishMs !== null && !Number.isNaN(finishMs) && now - finishMs > FREEZE_AFTER_MS;
}
