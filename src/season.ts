// Гонка флипов сезона. YEAR продьюсеров флипается 1 января по часам раннера,
// а «истина» о сезоне у источников — каждая в своё время: Jolpica переключает
// алиас «current», когда зальёт новый сезон в базу (бывает и до, и после
// Нового года), FIA/fiawec/Al Kamel заводят сезонные страницы к первому этапу.
// В окне рассинхрона читатель зеркала current.json видит расписание ЧУЖОГО
// сезона — и без сверки писал бы файлы нового года по прошлогоднему календарю
// (см. январские отравления fia/winners и порчу архива milestones).

/// Расписание в зеркале — за другой сезон, чем YEAR продьюсера: прогон надо
/// пропустить. Пустое/отсутствующее поле сезона — НЕ рассинхрон (нет данных —
/// нет и решения; вызывающие обрабатывают пустое расписание по-своему).
export function scheduleSeasonMismatch(
  season: string | null | undefined,
  year: number,
): boolean {
  return !!season && season !== String(year);
}
